#include <iostream>
#include <string>
using namespace std;

int main() {
    int integer = 5;
    float floatingPoint = 5.99;
    double doublePrecision = 9.98;
    char character = 'D';
    bool boolean = true;
    string str = "Hello";

    cout << "int : " << integer << endl;
    cout << "float : " << floatingPoint << endl;
    cout << "double : " << doublePrecision << endl;
    cout << "char : " << character << endl;
    cout << "bool : " << boolean << endl;
    cout << "string : " << str << endl;

    return 0;
}